#include <stdbool.h>
#include <stddef.h>
#include <stdint.h>

//---------------------------------------------------------------------------
// ● 根据地址写入内存
//---------------------------------------------------------------------------
void write_memory(int address, char value) {
	*((char*) address) = value;
}

//---------------------------------------------------------------------------
// ● 主程序
//---------------------------------------------------------------------------
void kernel_main() {
	int i;
	for (i = 0xa0000; i < 0xaffff; i++) {
		write_memory(i, i & 0xff);
	}
}
